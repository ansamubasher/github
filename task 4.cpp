#include <iostream>
using namespace std;
int main(){

    for (int i=1; i<=3;i++){  //loop 1
        for (int j=1; j<=3; j++){
            for (int k=1; k<=3; k++){
                cout<<i<<j<<k<<endl;  //displays all the possible combinations
            }
        }
    }




    return 0;
}
